void main(){
  print("Assignment operators");
  print("==========");
  const int n1 = 11;
  const int n2 = 5;
  print("n1+n2 = ${n1 + n2}");//1.Add
  print("n1-n2 = ${n1 - n2}"); //2.Subtraction = 5
  print("n1*n2 = ${n1 * n2}");//3.Multication = 50
  print("n1/n2 = ${n1 / n2}");//4.Division = 2.0
  print("n1%n2 = ${n1 % n2}");//5.Modulus = 0
}